public class ConfirmRegisterationInputModel extends CommonAPIInputModel {

    public String buildName;
    public String roomNo;
    public String floorNo;
    public String addressNo;
    public String soi;
    public String village;
    public String mooNo;
    public String street;
    public String tambol;
    public String amphur;
    public String province;
    public String postcode;
    public String contractNo;

    public ConfirmRegisterationInputModel() {
    }

    public ConfirmRegisterationInputModel(String nid, String version, String lang, String buildName,
            String roomNo, String floorNo, String addressNo, String soi,//
            String village, String mooNo, String street, String tambol,
            String amphur, String province, String postcode, String contractNo) {
        this.nid = nid;
        this.language = lang;
        this.version = version;
        this.buildName = buildName;
        this.roomNo = roomNo;
        this.floorNo = floorNo;
        this.addressNo = addressNo;
        this.soi = soi;//
        this.village = village;
        this.mooNo = mooNo;
        this.street = street;
        this.tambol = tambol;
        this.amphur = amphur;
        this.province = province;
        this.postcode = postcode;
        this.contractNo = contractNo;
    }
    public ConfirmRegisterationInputModel(String nid, String buildName,
            String roomNo, String floorNo, String addressNo,
            String village, String mooNo, String street, String tambol,
            String amphur, String province, String postcode, String contractNo) {
        this.nid = nid;
        this.buildName = buildName;
        this.roomNo = roomNo;
        this.floorNo = floorNo;
        this.addressNo = addressNo;
        this.village = village;
        this.mooNo = mooNo;
        this.street = street;
        this.tambol = tambol;
        this.amphur = amphur;
        this.province = province;
        this.postcode = postcode;
        this.contractNo = contractNo;
    }
}
