package com.promptnow.rdsmart.api.model;

/**
 *
 * @author Promptnow11
 */
public class ChangeOnlyPasswordInputModel extends CommonAPIInputModel{
    public String password;

    public ChangeOnlyPasswordInputModel() {
    }
    public ChangeOnlyPasswordInputModel(String nid, String lang, String authenKey, String password, String version) {
        this.nid = nid;
        this.password = password;
        this.version = version;
        this.language = lang;
        this.authenKey = authenKey;
    }
    
    
}
