package com.revenuedepartment.app;

public class DataForShared {
	public static String lastAccessed = "";
	public static String name = "";
	public static String surname = "";
	public static String nid = "";
	public static String getNidFormat(){
		if(nid.length()==13){
			nid = nid.substring(0, 1)+"-"+nid.substring(1, 5)+"-"+nid.substring(5, 10)+"-"+nid.substring(10, 12)+"-"+nid.substring(12, 13);
			return nid;
		}else {
			return nid; 
		}
	}
}
